function solution(people, limit) {
  let answer = 0;
  let sorted = people.sort((a,b) => a-b);

  let left = 0;
  let right = people.length -1;

  while(left <= right) {
    if(sorted[left] + sorted[right] <= limit) {
      left++;
      right--;
    } else right--;
    answer++;
  }

  return answer;
}

